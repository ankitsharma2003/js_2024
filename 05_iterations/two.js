// while loop

let index = 1;

while (index <= 10) {
    // console.log(`index is : ${index}`);
    index++;
}


// array with while
let myArr = ["Car", "Mobile", "House"];

let arr = 0;

while (arr < myArr.length) {
    // console.log('My array is : ', myArr[arr]);
    arr++
}



// do-while loop

let score = 1;
do {
  console.log(score);
  score++
} while (score <= 10);
    
